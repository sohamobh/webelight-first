//arrow functions provide us an alternative way to write a more concise and shorter syntax compared to the traditional function expression.
let addition = (x, y) => x + y;
console.log(addition(10, 20));

greet = (name) => 'Hello ' + name; //curly braces are not required for single line functions, round braces are not required for single argument.
console.log(greet("Soha"));